let diasDeSemana: string[] = ['Lunes', 'Martes', 'Miércoles', 'Jueves', 'Viernes', 'Sábado', 'Domingo'];

console.log(diasDeSemana[0]);

// push() -> agrega el elemento al final del array
diasDeSemana.push('Octes');
